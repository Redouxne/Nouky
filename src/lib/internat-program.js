export const PROGRAM_OPTIONS = Object.entries(INTERNAT_PROGRAM).map(([id, item]) => ({
  id,
  label: item.label,
}));

export function getProgramSubject(subjectId) {
  return INTERNAT_PROGRAM[subjectId] || INTERNAT_PROGRAM.rhumatologie;
}

export function getSkillLabel(skillId) {
  const parts = String(skillId).split(".");
  const subject = INTERNAT_PROGRAM[parts[0]];
  if (subject) {
    const directKey = parts.slice(1).join(".");
    const underscoredKey = parts.slice(1).join("_");
    if (subject.skills[directKey]) return subject.skills[directKey];
    if (subject.skills[underscoredKey]) return subject.skills[underscoredKey];
    for (const key of parts.slice(1)) {
      if (subject.skills[key]) return subject.skills[key];
    }
  }

  for (const item of Object.values(INTERNAT_PROGRAM)) {
    for (const key of parts.slice(1)) {
      if (item.skills[key]) return item.skills[key];
    }
  }
  return skillId;
}
